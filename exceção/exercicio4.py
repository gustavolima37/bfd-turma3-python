# Peça ao usuário para digitar uma palavra e:
# Mostre a palavra em maiúsculas (função upper()).
# Mostre a palavra em minúsculas (função lower()).
# Mostre quantas letras a palavra tem (função len()).

palavra = input('Digite uma palavra: ')

print(f'Palavra em maiuscula: {palavra.upper()}')
print(f'Palavra em minuscula: {palavra.lower()}')
print(f'Quantidade de letras na Palada: {len(palavra)}')
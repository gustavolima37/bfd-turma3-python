#Tipos de Variáveis

nome_cliente = "André Ribeiro" #String
print("Nome: ", nome_cliente)

idade_cliente = 18 #Inteiro
print("Idade: ", idade_cliente)

altura_cliente = 1.81 #Float
print("Altura do cliente ", altura_cliente)

status = True #Boolean
print("Situação: ", status)

status = False
print("Situação: ", status)

lista = [2,4,5,7,9] #Lista
print("Lista")
print("Lista: ", lista)
print("Lista: ", lista[3])

clientes = {"nome": "André", #Dicionário
            "idade": 26}
print("Dicionário")
print("Nome: ", clientes["nome"])
print("Idade: ", clientes["idade"])

#Valor nulo
resultado = None
print("Resultado: ", resultado)